export function getRankTier(score) {
  return RANK_TIERS.find(t => score >= t.minScore && score <= t.maxScore) || RANK_TIERS[0];
}
export function getNextRankTier(score) {
  const current = getRankTier(score);
  const idx = RANK_TIERS.indexOf(current);
  return idx < RANK_TIERS.length - 1 ? RANK_TIERS[idx + 1] : null;
}

